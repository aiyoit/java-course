package operator;

public class ComparisonOperator {

    public static void main(String[] args) {

        // Comparison Operator Example

        int a = 10;
        int b = 10;

        boolean r = (a > 1);
        System.out.println(r);

        // Equals to operator
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
